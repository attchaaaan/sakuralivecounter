function escapeHtml(str) {
  return str.replace(/[&<>"']/g, (c) => {
    const map = {
      "&": "&amp;",
      "<": "&lt;",
      ">": "&gt;",
      '"': "&quot;",
      "'": "&#39;",
    };
    return map[c];
  });
}

// パーセンテージ表示を作る（小数1桁）
function formatRate(part, total) {
  if (!total) return "0.0";
  return ((part / total) * 100).toFixed(1);
}

