function renderLiveList() {
  liveListEl.innerHTML = "";

  // 日付（id: YYYYMMDD）で新しい順に並べ替えて表示
  const sortedLives = Array.from(lives).sort((a, b) => String(b.id).localeCompare(String(a.id)));

  for (const live of sortedLives) {
    const row = document.createElement("div");
    row.className = "live-row";

    const label = document.createElement("label");

    const cb = document.createElement("input");
    cb.type = "checkbox";
    cb.value = live.id;
    cb.addEventListener("change", update);

    const name = document.createElement("span");
    name.textContent = live.name;

    // 日付表示（そのまま）
    const dateSpan = document.createElement("span");
    dateSpan.className = "live-date";
    if (live.date) {
      dateSpan.textContent = live.date;
    }

    label.appendChild(cb);
    label.appendChild(dateSpan);
    label.appendChild(name);

    const btn = document.createElement("button");
    btn.type = "button";
    btn.textContent = "セトリ表示";
    btn.addEventListener("click", () => openSetlistModal(live));

    row.appendChild(label);
    row.appendChild(btn);

    liveListEl.appendChild(row);
  }
}


// =====================
// 4) 集計して表示
// =====================
function update() {
  const checkedIds = Array.from(
    liveListEl.querySelectorAll('input[type="checkbox"]:checked')
  ).map((i) => i.value);

  const selectedLives = lives.filter((l) => checkedIds.includes(l.id));

    // ===== 参加率（全体＋年別） =====
  const totalLives = lives.length;                 // 開催数（登録しているライブ数）
  const participatedLives = selectedLives.length;  // 参加数（チェック数）

  // 年別の開催数/参加数
  const byYear = new Map(); // year -> { total, participated }

  for (const live of lives) {
    if (!live) continue; // undefined チェック
    const y = getYearFromLive(live);
    const cur = byYear.get(y) ?? { total: 0, participated: 0 };
    cur.total += 1;
    byYear.set(y, cur);
  }

  for (const live of selectedLives) {
    if (!live) continue; // undefined チェック
    const y = getYearFromLive(live);
    const cur = byYear.get(y) ?? { total: 0, participated: 0 };
    cur.participated += 1;
    byYear.set(y, cur);
  }

  // 表示用：unknownは最後、年は降順
  const yearRows = Array.from(byYear.entries())
    .sort((a, b) => {
      if (a[0] === "unknown") return 1;
      if (b[0] === "unknown") return -1;
      return Number(b[0]) - Number(a[0]);
    });

  const allLine = `all: ${participatedLives} / ${totalLives} (${formatRate(participatedLives, totalLives)} %)`;

  // 表示要素を分割してレスポンシブに制御できるようにする
  const yearLines = yearRows
    .filter(([year]) => year !== "unknown")
    .map(([year, v]) => `${year}: ${v.participated} / ${v.total} (${formatRate(v.participated, v.total)} %)`);

  // 各要素を独立した行で表示する
  const parts = [`<div class="part all">${escapeHtml(allLine)}</div>`, ...yearLines.map((y) => `<div class="part year">${escapeHtml(y)}</div>`)];
  participationEl.innerHTML = `<div class="row"><div class="label">参加率</div><div class="value">${parts.join("")}</div></div>`;

  // songId -> count と songId -> lastDate
  const counts = new Map();
  const lastDate = new Map();

  for (const live of selectedLives) {
    for (const songId of live.setlist) {
      // 未登録IDが混ざってても落ちないようにする（入力ミス対策）
      if (!songs[songId]) continue;

      counts.set(songId, (counts.get(songId) ?? 0) + 1);
      // 最後の日付を更新（後のライブが新しい日付を上書き）
      if (live.date) {
        lastDate.set(songId, live.date);
      }
    }
  }

  // ソート：sortBy と sortOrder に応じて切り替え
  const allSongIds = Object.keys(songs).map(Number);
  const rows = songViewMode === "unseen"
    ? allSongIds
        .filter((songId) => !counts.has(songId))
        .sort((a, b) => songs[a].name.localeCompare(songs[b].name, "ja"))
        .map((songId) => [songId, 0])
    : Array.from(counts.entries()).sort((a, b) => {
        let compareResult = 0;
        
        if (sortBy === "date") {
          // 日付でソート
          const dateA = lastDate.get(a[0]) ?? "";
          const dateB = lastDate.get(b[0]) ?? "";
          compareResult = dateB.localeCompare(dateA); // 新しい順がデフォルト
        } else {
          // 回数でソート
          compareResult = b[1] - a[1]; // 多い順がデフォルト
        }
        
        // 主キーで差がない場合は曲名昇順
        if (compareResult === 0) {
          return songs[a[0]].name.localeCompare(songs[b[0]].name, "ja");
        }
        
        // sortOrderが"asc"なら反転
        return sortOrder === "asc" ? -compareResult : compareResult;
      });

  if (lastDateHeaderEl) {
    lastDateHeaderEl.style.display = songViewMode === "unseen" ? "none" : "";
  }

  resultBodyEl.innerHTML = rows
    .map(([songId, n]) => {
      const name = songs[songId].name;
      const date = lastDate.get(songId) ?? "";
      const dateCell = songViewMode === "unseen" ? "" : `<td>${escapeHtml(date)}</td>`;
      return `<tr><td>${escapeHtml(name)}</td><td>${n}</td>${dateCell}</tr>`;
    })
    .join("");

  const totalSongs = rows.reduce((sum, [, n]) => sum + n, 0);

  if (songViewMode === "unseen") {
    summaryEl.textContent = `ライブ${selectedLives.length}件 / 見たことない曲${rows.length}曲`;
  } else {
    summaryEl.textContent = `ライブ${selectedLives.length}件 / 総${totalSongs}曲 / ユニーク${rows.length}曲`;
  }
}

